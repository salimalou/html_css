for (var nombre = 1; nombre <= 100; nombre++) {
  if (nombre % 3 === 0 && nombre % 5 === 0) {
    document.write("FizzBuzz");
  } else if (nombre % 3 === 0) {
    document.write("Fizz");
  } else if (nombre % 5 === 0) {
    document.write("Buzz");
  } else {
    document.write;
  }
}
